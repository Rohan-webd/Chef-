<h1>Passion For<br> Concoctions</h1>
<p>Born and brought up in Daryaganj, a place where the aroma of
Indian and traditional food always lingers around and where
concoctions cooked in old, rustic shops take no time to tempt
your soul, Chef Vaibhav always had a penchant for food since
long before he knew what dreams and ambitions are made of.
Little did he know that Daryaganj was only the start.
<br><br> Frequent
and infrequent visits to ‘The Lalit’ with his uncle fuelled his
zest and by the time he began understanding the concept of
career, work and profession, he had already made a recipe to
the dream-future in his head. Over the time, his passion for
food stirred more, metamorphosed into a profession and
inspiration for many aspiring chefs waiting to be called on the
other side of the table.</p>
