and foodies, Chef Vaibhav has created a
legacy of his name and is one of the
most celebrated culinary artists of India
who has upped the food-scene of the
country nationally and internationally
with his lip-smacking innovations.
<br><br>
This is to all the fusions, menus, recipes
and restaurants coming from Chef
Vaibhav’s brilliant culinary mind,
soothing people’s taste buds for years.</p>
